// Identifier, Literal, Operators, Expressions, Keywords
var _a = 10; // _a is an identifier, 10 is a literal
var $b = 20;
console.log(_a + $b); // _a and $b are identifiers, + is an operator, _a + $b is an expression

// Keywords: Reserved words in JavaScript that have a specific meaning and cannot be used as identifiers. 
// Examples: var, let, const, function, if, else, return, etc.

// Identifier: A name used to identify a variable, function, or property in JavaScript.
// It must start with a letter, underscore (_), or dollar sign ($) and can be followed by letters, digits, underscores, or dollar signs. Examples: myVariable, _myFunction, $myProperty.

// Literal: A fixed value that is directly represented in the code. 
// Examples: 42 (number literal), "Hello" (string literal),true (boolean literal).
